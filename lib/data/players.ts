import { Player, PlayerAttributes } from "@/lib/types";

type DeepPartial<T> = { [K in keyof T]?: T[K] extends object ? DeepPartial<T[K]> : T[K] };

function attrs(overrides: Partial<PlayerAttributes> = {}): PlayerAttributes {
  return {
    technique: 10, power: 10, timing: 10, placement: 10, running: 12,
    pace: 8, swing: 8, seam: 8, spin: 8, flight: 8, accuracy: 10, variation: 8,
    catching: 13, throwing: 12, agility: 13,
    composure: 12, leadership: 10, determination: 13,
    ...overrides,
  };
}

function mkPlayer(p: {
  id: string; name: string; age: number;
  nat: "Indian" | "Overseas";
  role: Player["role"];
  bat: "Right-hand" | "Left-hand";
  bowl: string | null;
  star: number;
  base: number;
  capped?: boolean;
  teamId: string;
  price?: number;
  potential?: Player["potential"];
  attributes: PlayerAttributes;
  batStats?: { matches: number; innings: number; runs: number; avg: number; sr: number; fifties: number; hundreds: number };
  bowlStats?: { matches: number; wickets: number; econ: number; avg: number; best: string };
}): Player {
  return {
    id: p.id,
    name: p.name,
    age: p.age,
    nationality: p.nat,
    role: p.role,
    battingStyle: p.bat,
    bowlingStyle: p.bowl,
    attributes: p.attributes,
    starRating: p.star,
    basePrice: p.base,
    isCapped: p.capped !== false,
    isRetained: false,
    retainedByTeamId: null,
    currentTeamId: p.teamId,
    potential: p.potential ?? "Established",
    iplHistory: [{ teamId: p.teamId, season: "2024", price: p.price ?? p.base }],
    careerStats: {
      batting: p.batStats
        ? { matches: p.batStats.matches, innings: p.batStats.innings, runs: p.batStats.runs, average: p.batStats.avg, strikeRate: p.batStats.sr, fifties: p.batStats.fifties, hundreds: p.batStats.hundreds }
        : { matches: 0, innings: 0, runs: 0, average: 0, strikeRate: 0, fifties: 0, hundreds: 0 },
      bowling: p.bowlStats
        ? { matches: p.bowlStats.matches, wickets: p.bowlStats.wickets, economy: p.bowlStats.econ, average: p.bowlStats.avg, bestFigures: p.bowlStats.best }
        : { matches: 0, wickets: 0, economy: 0, average: 0, bestFigures: "0/0" },
    },
  };
}

